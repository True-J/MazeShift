var mapid = document.getElementById("mapid")
var level = document.getElementById("level")

function mapchange() {
    var roomnumber = parseInt(mapid.value);
    var mazelevel = parseInt(level.value);

    if (roomnumber == 44) {
        var mapped = map[0][0]
    } else {
        if (shiftrooms.indexOf(roomnumber) != -1){
            mazelevel = ((mazelevel+1)%4)
            level.value = mazelevel;
        };
        mapped = map[mazelevel][room[mazelevel][roomnumber]];
    };          

    for (var i=0; i<mapped.length; i++) {
        for (var j=0; j<mapped[i].length; j++) {
            var boxname = "box" + i + j;
            document.getElementById(boxname).style.background = mapped[i][j] ? "black" : "white";
        };
    };
    
    var dirlist = [
        ["N to Room ","NE to Room ","E to Room ","SE to Room ","S to Room ","SW to Room ","W to Room ","NW to Room "],
        ["W to Room ","NW to Room ","N to Room ","NE to Room ","E to Room ","SE to Room ","S to Room ","SW to Room "],
        ["S to Room ","SW to Room ","W to Room ","NW to Room ","N to Room ","NE to Room ","E to Room ","SE to Room "],
        ["E to Room ","SE to Room ","S to Room ","SW to Room ","W to Room ","NW to Room ","N to Room ","NE to Room "]
    ];

    if (roomnumber == 44) {
        document.getElementById("b0").style.display = "block";
        document.getElementById("b0").innerHTML = "E to Room 1";
        for (i=1; i<8; i++) {
            document.getElementById("b"+i).style.display = "none";
        };
    } else {
        for (var i=0; i<8; i++) {
            var x = document.getElementById("b"+i);
            if (hallway[i][((roomnumber+(11*mazelevel))%44)] == 0) {
                x.style.display = "none";
            } else {
                x.style.display = "block";
                if (roomnumber == 0 && mazelevel == 0 && i == 4) {
                    x.innerHTML = "S to Exit";
                } else {
                    if (roomnumber == 41 && mazelevel == 2 && i == 4) {
                        x.innerHTML = "N to [3RR0R]"
                    } else {
                        if ((hallway[i][((roomnumber+(11*mazelevel))%44)] + (33*mazelevel))%44 == 0) {
                            x.innerHTML = dirlist[mazelevel][i] + 44;
                        } else {
                            x.innerHTML = dirlist[mazelevel][i] + (hallway[i][((roomnumber+(11*mazelevel))%44)] + (33*mazelevel))%44;
                        };
                    };
                };
            };
        };
    };
};

function roomchange(d) {
    var mazelevel = parseInt(level.value);
    var roomnumber = parseInt(mapid.value);
    
    if (roomnumber == 44) {
        mapid.value = 1;
        mapchange();
    } else {
        if (roomnumber == 0 && mazelevel == 0 && d == 4) {
            exitmaze();
        } else {
            if (roomnumber == 41 && mazelevel == 2 && d == 4) {
                var mapped = map[2][24];
                for (var i=0; i<mapped.length; i++) {
                    for (var j=0; j<mapped[i].length; j++) {
                        var boxname = "box" + i + j;
                        document.getElementById(boxname).style.background = mapped[i][j] ? "black" : "white";
                    };
                };
                for (i=0; i<8; i++) {
                    document.getElementById("b"+i).style.display = "none";
                };
                document.getElementById("restart").style.display = "block";
            } else {
                mapid.value = (hallway[d][((roomnumber+(11*mazelevel))%44)] + (33*mazelevel))%44;
                mapchange();
            };
        };
    };
};

function exitmaze(){
    mapid.value = 44;
    level.value = 0;
    document.getElementById("restart").style.display = "none";
    mapchange();
};

function looplevel(){
    if (parseInt(level.value) > 3 ){
        level.value = 0;
    }
};

level.addEventListener("change", function(){
    looplevel();
});

